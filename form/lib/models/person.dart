class Person {
  final String name;
  final String email;
  final String address;
  final String phoneNumber;
  final String birthDate;
  final String gender;
  final String job;

  Person({
    required this.name,
    required this.email,
    required this.address,
    required this.phoneNumber,
    required this.birthDate,
    required this.gender,
    required this.job,
  });
}
